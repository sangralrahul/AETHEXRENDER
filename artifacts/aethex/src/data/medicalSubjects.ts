export function getSubjectBySlug(slug: string): MedicalSubject | undefined {
  return medicalSubjects.find(s => s.slug === slug);
}

export function getTopicBySlug(subjectSlug: string, topicSlug: string): { subject: MedicalSubject; topic: Topic } | undefined {
  const subject = getSubjectBySlug(subjectSlug);
  if (!subject) return undefined;
  const topic = subject.topics.find(t => t.slug === topicSlug);
  if (!topic) return undefined;
  return { subject, topic };
}

export const totalTopics = medicalSubjects.reduce((sum, s) => sum + s.topics.length, 0);
