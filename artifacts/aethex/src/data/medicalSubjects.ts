export interface Topic {
  name: string;
  slug: string;
  difficulty: "Basic" | "Intermediate" | "Advanced";
  readTime: number;
  relatedConditions?: string[];
}

export interface MedicalSubject {
  id: string;
  name: string;
  slug: string;
  category: "Pre-Clinical" | "Para-Clinical" | "Clinical";
  icon: string;
  color: string;
  description: string;
  topics: Topic[];
}

function toSlug(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}

function makeTopic(name: string, difficulty: Topic["difficulty"] = "Intermediate", readTime = 15, relatedConditions?: string[]): Topic {
  return { name, slug: toSlug(name), difficulty, readTime, relatedConditions };
}

